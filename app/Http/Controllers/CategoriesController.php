use Illuminate\Http\Request;

class CategoriesController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categories = Categories::simplepaginate(5);
        return view('layouts.categories.index', ['categories' => $categories]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categories = Categories::simplepaginate(5);
        return view('layouts.categories.create', ['categories' => $categories]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CreateCategories $request)
    {
        $credentials = [
            'author'    => auth()->user()->name,
            'name'     => $request->name,
        ];

        Categories::create($credentials);
        return redirect()->route('categories.create');
    }

