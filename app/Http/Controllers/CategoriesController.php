    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $categories = Categories::findorfail($id);
        $categories->delete();
        return redirect()->route('categories.create');
    }
}
